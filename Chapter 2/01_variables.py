# Variables in python are case sensitive 
a = "Honey" # string 
b = 345     # integer
c = 45.32   # float 
d = True
e = None

# Printing the variables 
print(a)
print(b)
print(c)
print(d)
print(e)

# Printing the type of variables 
print(type (a))
print(type (b))
print(type (c))
print(type (d))
print(type (e))