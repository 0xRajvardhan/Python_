myDict = {
    "fast": "In a quick manner",
    "honey": "A programer",
    "marks": [1, 2, 5],
    "anotherdict": {'honey': 'developer'}  # nested dictionarty
}
'''
dictionaries are unordered 
dictionaries are mutable 
keys cannot be duplicated or repeatable
'''
print(myDict['fast'])
print(myDict['honey'])
print(myDict['marks'])
print(myDict['anotherdict']['honey'])
