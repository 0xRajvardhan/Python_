# prints numbers from 0 to 7
for i in range(8):
    print(i)
print("\n")

# if you want it to start from a partiular number so mention it like this
for i in range(2, 8):
    print(i)
print("\n")
#  if you wish to skip numbers in a regular interval you can write it like this
for i in range(2, 8, 2):
    print(i)
