# if elif else ladder
if (a < 3):

    print("The value of a is greater than 3")

elif (a < 7):

    print("The value of a is greater than 7")

elif (a > 13):

    print("The value of a is greater than 7")

elif (a > 17):

    print("The value of a is greater than 17")

else:
    print("The value is not greater than 3 or 7")

