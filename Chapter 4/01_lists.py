# Create list using big brackets
a = [1, 2, 4, 56, 6]
print(a)

print(a[0])
print(a[2])
# Changed the value using this
a[0] = 98
print(a)
# We can create lists with items of different types
c = [45, "Honey", False, 6.9]
print(c)

# Slicing in lists
friends= ["Harry", "Tom", "Rohan", "Sam", "Divya", 45]
print(friends[:4])
print(friends[-4:])